#!/usr/bin/env python3
"""Regenerate references/rubrics.md from judge/rubrics.py."""

from __future__ import annotations

import sys
from pathlib import Path

_REPO_ROOT = Path(__file__).resolve().parent.parent
if str(_REPO_ROOT) not in sys.path:
    sys.path.insert(0, str(_REPO_ROOT))

from judge.rubrics import RUBRICS

# Section order and H2 subtitles (display only; criteria come from RUBRICS).
_TASK_ORDER: tuple[str, ...] = (
    "MedCOT",
    "MedCallAPI",
    "MedCollab",
    "MedDBOps",
    "MedDecomp",
    "MedDefend",
    "MedLongConv",
    "MedLongQA",
    "MedPathPlan",
    "MedReflect",
    "MedRetAPI",
    "MedRoleAdapt",
    "MedShield",
)

_SUBTITLE: dict[str, str] = {
    "MedCOT": "Chain-of-Thought Clinical Reasoning",
    "MedCallAPI": "Clinical API Call Generation",
    "MedCollab": "Multi-System Medical Collaboration",
    "MedDBOps": "Medical Database Operations",
    "MedDecomp": "Clinical Task Decomposition",
    "MedDefend": "Adversarial Medical Input Defense",
    "MedLongConv": "Long Medical Conversation",
    "MedLongQA": "Long Medical Document Q&A",
    "MedPathPlan": "Clinical Pathway Planning",
    "MedReflect": "Clinical Error Detection & Reflection",
    "MedRetAPI": "Medical Knowledge Retrieval",
    "MedRoleAdapt": "Medical Role Adaptation",
    "MedShield": "Medical Safety Risk Intervention",
}


def _esc(cell: str) -> str:
    """Escape pipes for GitHub-flavored markdown tables."""
    return " ".join(cell.split()).replace("|", "\\|")


def _render_rubric(rubric: Rubric) -> str:
    lines: list[str] = []
    task = rubric.task
    sub = _SUBTITLE.get(task, task)
    lines.append(f"## {task} — {sub}")
    lines.append("")
    lines.append(f"*{rubric.description.strip()}*")
    lines.append("")
    lines.append("| Criterion | Description | Score 1 | Score 5 |")
    lines.append("|-----------|-------------|---------|---------|")
    for c in rubric.criteria:
        row = (
            f"| {_esc(c.name)} | {_esc(c.description)} | "
            f"{_esc(c.score_1)} | {_esc(c.score_5)} |"
        )
        lines.append(row)
    lines.append("")
    lines.append("---")
    lines.append("")
    return "\n".join(lines)


def main() -> None:
    missing = [t for t in _TASK_ORDER if t not in RUBRICS]
    if missing:
        print(f"ERROR: RUBRICS missing tasks: {missing}", file=sys.stderr)
        sys.exit(1)
    extra = set(RUBRICS.keys()) - set(_TASK_ORDER)
    if extra:
        print(f"ERROR: RUBRICS has tasks not in _TASK_ORDER: {sorted(extra)}", file=sys.stderr)
        sys.exit(1)

    parts: list[str] = [
        "# MedBench-Eval Task Rubrics",
        "",
        "Auto-generated by `python scripts/export_rubrics_md.py` from `judge/rubrics.py`. "
        "Do not edit by hand — change the Python definitions and re-run the script.",
        "",
        "Each criterion is scored 1 (worst) to 5 (best) on a Likert scale.",
        "",
        "---",
        "",
    ]
    for task in _TASK_ORDER:
        parts.append(_render_rubric(RUBRICS[task]))

    parts.extend(
        [
            "## Scoring Reference",
            "",
            "| Normalized Score | Interpretation |",
            "|-----------------|----------------|",
            "| 90-100 | Exceptional — meets or exceeds gold standard |",
            "| 75-89 | Strong — minor gaps only |",
            "| 60-74 | Adequate — some important gaps |",
            "| 40-59 | Weak — significant deficiencies |",
            "| 0-39 | Poor — major failures |",
            "",
            "**Formula:** `normalized = (average criterion score − 1) / 4 × 100`, "
            "where each criterion is 1–5.",
            "",
        ]
    )

    out = _REPO_ROOT / "references" / "rubrics.md"
    out.write_text("\n".join(parts), encoding="utf-8")
    print(f"Wrote {out}")


if __name__ == "__main__":
    main()
